package navigation;

public class FoldingAndExpandingCode {

    public void firstMeethod(){
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }
    }

    public void secondtMeethod(){
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }

        for (int j = 0; j < 10; j++) {
            System.out.println("i = " + j);
        }
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }

    }

}
